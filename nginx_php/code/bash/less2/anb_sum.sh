#!/bin/bash

# Цвета для вывода
RED='\033[0;31m'
GREEN='\033[0;32m'
# Нет цвета
NC='\033[0m'

# Проверка аргументов
if [ $# -ne 2 ]; then
    echo -e "${RED}Ошибка: требуется два аргумента${NC}"
    echo "Использование: $0 <число1> <число2>"
    echo "Возможно вводить целые и дробные числа"
    exit 1
fi

# Проверяем целые или дробные
if ! [[ "$1" =~ ^-?[0-9]+([.][0-9]+)?$ ]] || ! [[ "$2" =~ ^-?[0-9]+([.][0-9]+)?$ ]]; then
    echo -e "${RED}Ошибка: оба аргумента должны быть целыми или дробными числами${NC}"
    exit 1
fi

# Вычисление и вывод
sum=$(awk -v a=$1 -v b=$2 'BEGIN {print a + b}')
echo -e "${GREEN}$1 + $2 = $sum${NC}"
